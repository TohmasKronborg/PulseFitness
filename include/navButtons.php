                <ul>
                    <li>1–5 reps</li>
                    <li>Lange pauser</li>
                    <li>Tung belastning</li>
                </ul>

                <p>
                    Bruges primært til at udvikle maksimal styrke.
                </p>

                <h4>Mellemområde</h4>

                <ul>
                    <li>6–12 reps</li>
                    <li>Moderat til tung vægt</li>
                </ul>

