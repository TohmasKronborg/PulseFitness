                    styrke, muskelmasse og kondition.
                </p>

                <h3>Reps (repetitioner)</h3>

                <p>
                    En <strong>rep</strong> er én gentagelse af en øvelse fra start til slut.
                </p>

                <p>
                    Eksempel:<br>
                    Hvis du laver én armbøjning, har du lavet 1 rep.<br>
                    Hvis du laver 10 armbøjninger i træk, har du lavet 10 reps.
                </p>

                <p>
                    Reps beskriver altså, hvor mange gange en bevægelse udføres uden pause.
                </p>

                <h3>Sets</h3>

                <p>
                    Et <strong>set</strong> er en gruppe reps udført i træk, før man holder pause.
                </p>

                <p>
                    Eksempel:
                </p>

                <ul>
                    <li>10 squats = 10 reps</li>
                    <li>Pause</li>
                    <li>10 squats igen = endnu et set</li>
                </ul>

                <p>
                    Hvis et program siger <strong>3 x 10</strong>, betyder det:
                </p>

                <ul>
                    <li>3 sets</li>
                    <li>10 reps i hvert set</li>
                </ul>

                <p>
                    Mellem sets holder man typisk pause i 30 sekunder til 3 minutter afhængigt
                    af træningsmålet.
                </p>

                <h3>Hvordan bruges sets og reps?</h3>

                <p>
                    Antallet af sets og reps ændrer træningens fokus.
                </p>

